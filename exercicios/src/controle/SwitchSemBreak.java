package controle;

public class SwitchSemBreak {
	public static void main(String[] args) {

		String faixa = "roxa";

		switch (faixa.toLowerCase()) {
		case "preta":
			System.out.println("Sei o bababa");
		case "marrom":
			System.out.println("Sei o cacaca");
		case "roxa":
			System.out.println("sei kkkk");
		default:
			System.out.println("chau galere");
		}

	}
}
